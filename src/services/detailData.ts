// 获取详情数据
export const getDetailData = async (section: string, itemId: string): Promise<DetailData> => {
  // 延迟模拟网络请求
  await new Promise(resolve => setTimeout(resolve, 500));
  
  switch (section) {
    case 'advantages':
      return advantagesData[itemId] || createEmptyDetail();
    case 'features':
      return featuresData[itemId] || createEmptyDetail();
    case 'scenarios':
      return scenariosData[itemId] || createEmptyDetail();
    case 'solutions':
      return solutionsData[itemId] || createEmptyDetail();
    default:
      return createEmptyDetail();
  }
};

